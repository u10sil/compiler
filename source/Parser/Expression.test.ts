import { Error } from "@cogneco/mend"
import * as SyntaxTree from "../SyntaxTree"
import * as Parser from "./"

describe("Parser.Expression", () => {
	const handler = new Error.ConsoleHandler()
	it("a 'b'", () => {
		const parser = Parser.create("a \"b\"", handler)
		expect(SyntaxTree.filterUndefined(SyntaxTree.filterId(parser.fetch()!.serialize()))).toEqual({
			class: "module",
			name: "unnamed",
			statements: [{
				class: "literal.typedObject",
				name: { class: "type.identifier", name: "a", parameters: undefined },
				value: { class: "literal.string", value: "b" },
			}],
		})
	})
	it("a b c", () => {
		const parser = Parser.create("a b c", handler)
		expect(SyntaxTree.filterId(parser.fetch()!.serialize())).toEqual({
			class: "module",
			statements: [
				{ class: "identifier", name: "a" },
				{ class: "identifier", name: "b" },
				{ class: "identifier", name: "c" },
			],
		})
	})
	it("a + b + c", () => {
		const parser = Parser.create("a + b + c", handler)
		expect(SyntaxTree.filterId(parser.fetch()!.serialize())).toEqual({
			class: "module",
			statements: [
				{
					class: "infixOperator", symbol: "+",
					left: {
						class: "infixOperator", symbol: "+",
						left: { class: "identifier", name: "a" },
						right: { class: "identifier", name: "b" },
					},
					right: { class: "identifier", "name": "c" },
				}],
		})
	})
	it("a.b.c", () => {
		const parser = Parser.create("a.b.c", handler)
		expect(SyntaxTree.filterId(parser.fetch()!.serialize())).toEqual({
			class: "module",
			statements: [
				{
					class: "resolvingOperator", symbol: ".",
					left: {
						class: "resolvingOperator", symbol: ".",
						left: { class: "identifier", name: "a" },
						right: { class: "identifier", name: "b" },
					},
					right: { class: "identifier", "name": "c" },
				}],
		})
	})
	it("a.b c", () => {
		const parser = Parser.create("a.b c", handler)
		expect(SyntaxTree.filterId(parser.fetch()!.serialize())).toEqual({
			class: "module",
			statements: [
				{
					class: "resolvingOperator", symbol: ".",
					left: { class: "identifier", name: "a" },
					right: { class: "identifier", name: "b" },
				},
				{ class: "identifier", "name": "c" },
			],
		})
	})
	it("a b = c", () => {
		const parser = Parser.create("a b = c", handler)
		expect(SyntaxTree.filterId(parser.fetch()!.serialize())).toEqual({
			class: "module",
			statements: [
				{ class: "identifier", "name": "a" },
				{
					class: "infixOperator", symbol: "=",
					left: { class: "identifier", name: "b" },
					right: { class: "identifier", name: "c" },
				},
			],
		})
	})
	it("a + b * c", () => {
		const parser = Parser.create("a + b * c", handler)
		expect(SyntaxTree.filterId(parser.fetch()!.serialize())).toEqual({
			class: "module",
			statements: [
				{
					class: "infixOperator", symbol: "+",
					left: { class: "identifier", "name": "a" },
					right: {
						class: "infixOperator", symbol: "*",
						left: { class: "identifier", name: "b" },
						right: { class: "identifier", name: "c" },
					},
				},
			],
		})
	})
	it("a * b + c", () => {
		const parser = Parser.create("a * b + c", handler)
		expect(SyntaxTree.filterId(parser.fetch()!.serialize())).toEqual({
			class: "module",
			statements: [
				{
					class: "infixOperator", symbol: "+",
					left: {
						class: "infixOperator", symbol: "*",
						left: { class: "identifier", name: "a" },
						right: { class: "identifier", name: "b" },
					},
					right: { class: "identifier", "name": "c" },
				},
			],
		})
	})
})
